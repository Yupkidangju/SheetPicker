class Translator:
    _current_lang = "kr"

    @classmethod
    def set_language(cls, lang):
        if lang in ["kr", "en", "jp", "cn_t", "cn_s"]:
            cls._current_lang = lang

    @classmethod
    def get(cls, key):
        lang_dict = STRINGS.get(key, {})
        return lang_dict.get(cls._current_lang, lang_dict.get("en", key))

    @classmethod
    def get_current_lang(cls):
        return cls._current_lang
